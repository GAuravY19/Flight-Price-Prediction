def Month_convert(month:str):
    Month = {
        'January':'Jan',
        "Feburary":'Feb',
        "March":'Mar',
        "April":'Apr',
        "May":'May',
        "June":'Jun',
        "July":'Jul',
        "August":'Aug',
        "September":'Sep',
        "October":'Oct',
        "November":'Nov',
        "December":'Dec'
    }

    return Month[month]
